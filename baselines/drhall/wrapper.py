    # ----- Answer-consistency assessment helpers (paper Section 3.5) -----

    @staticmethod
    def build_simplify_prompt(answer: str) -> str:
        """Build the paper's answer-simplification prompt."""
        return ANSWER_SIMPLIFY_PROMPT.format(answer=answer)

    @staticmethod
    def build_consistency_judge_prompt(
        original_answer: str, follow_up_answer: str
    ) -> str:
        """
        Build a consistency-judge prompt combining the two (processed) answers
        and the paper's exact judge instruction.
        """
        return (
            f"Answer A: {original_answer}\n"
            f"Answer B: {follow_up_answer}\n\n"
            f"{CONSISTENCY_JUDGE_PROMPT}"
        )

    @staticmethod
    def parse_consistency(verdict_text: str) -> Optional[bool]:
        """
        Parse the consistency-judge verdict.

        Returns ``True`` if consistent, ``False`` if inconsistent, ``None`` if
        unparseable. The paper maps Yes->consistent, No->inconsistent.
        """
        if not verdict_text or not str(verdict_text).strip():
            return None
        text = str(verdict_text).strip().lower()
        # Prefer an exact-ish leading token.
        first = text.split()[0] if text.split() else ""
        if first in {"yes", "consistent", "right", "correct"}:
            return True
        if first in {"no", "inconsistent", "wrong", "not"}:
            return False
        if "not" in text and "consistent" in text:
            return False
        if "consistent" in text or "yes" in text:
            return True
        if "no" in text or "wrong" in text:
            return False
        return None

    # ----- Internal helpers -----

    # ----- AMR1 answer-declarativization helpers (boolean / MC / WH) -----
    # The paper's AMR1 negates the LLM's *declarative* answer. Our KG-rule
    # dataset answers are short labels, so we must reconstruct a statement per
    # question type. These helpers are deterministic (no LLM).

    @staticmethod
    def _classify_question_type(
        question: str, row: Optional[pd.Series]
    ) -> str:
        """Classify a row into 'boolean' | 'multiple_choice' | 'wh_question'.

        Prefers the dataset's explicit ``original_question_type`` tag; falls
        back to regex on the question text.
        """
        qt = _safe_get(row, "original_question_type").strip().lower()
        if qt in ("true_false", "yes_no"):
            return "boolean"
        if qt == "multiple_choice":
            return "multiple_choice"
        if qt == "wh_question":
            return "wh_question"
        # Infer from question text.
        q = (question or "").strip().lower()
        if re.search(r"\bis it (?:true|the case) that\b", q):
            return "boolean"
        if "which of the following" in q:
            return "multiple_choice"
        return "wh_question"

    @staticmethod
    def _extract_boolean_proposition(question: str) -> Optional[str]:
        """Extract proposition P from "... is it true that P?".

        Returns None if the boolean-question pattern is not found.
        """
        m = re.search(
            r"\bis it (?:true|the case) that\s+(.+?)\?\s*$",
            question or "",
            re.IGNORECASE | re.DOTALL,
        )
        if not m:
            return None
        return m.group(1).strip().strip(".").strip()

    @staticmethod
    def _parse_boolean_verdict(answer: Any) -> Optional[bool]:
        """Parse the LLM's boolean answer into True/False/None."""
        if _is_missing(answer):
            return None
        t = str(answer).strip().lower()
        if not t:
            return None
        first = re.split(r"[\s,.;:!?]+", t, maxsplit=1)[0]
        if first in ("true", "yes", "correct", "affirmative", "right"):
            return True
        if first in ("false", "no", "incorrect", "wrong", "negative"):
            return False
        if re.search(r"\bnot (?:true|the case)\b", t) or "is false" in t:
            return False
        if "is true" in t or "it is true" in t:
            return True
        if "false" in t:
            return False
        if "true" in t:
            return True
        return None

    @staticmethod
    def _parse_mc_options(
        row: Optional[pd.Series], question: str = ""
    ) -> List[str]:
        """Parse MC option texts in A/B/C/D order.

        Prefers the dataset's ``original_options`` JSON list (index 0 == A);
        falls back to parsing 'A. ... B. ...' from the question.
        """
        if row is not None:
            raw = row.get("original_options", "")
            if not _is_missing(raw):
                s = str(raw).strip()
                if s and s != "[]":
                    try:
                        opts = json.loads(s)
                        if isinstance(opts, list) and opts:
                            return [str(o).strip() for o in opts if str(o).strip()]
                    except (json.JSONDecodeError, TypeError):
                        pass
        # Fallback: parse from the question body.
        if question:
            matches = re.findall(
                r"\b([A-D])\.\s*(.+?)(?=\n\s*[A-D]\.|\s*$)",
                question,
                re.DOTALL,
            )
            opts = [text.strip() for _, text in matches if text.strip()]
            if opts:
                return opts
        return []

    @staticmethod
    def _parse_mc_option_index(answer: Any, n_options: int) -> Optional[int]:
        """Map an LLM MC answer to a 0-based option index, or None.

        Handles 'A', 'a', '(A)', 'A.', 'Option A', etc.
        """
        if _is_missing(answer) or n_options <= 0:
            return None
        t = str(answer).strip()
        m = re.match(r"^\(?([A-Da-d])\)?[.\):\s]", t)
        if not m:
            m = re.match(r"^([A-Da-d])$", t)
        if m:
            idx = ord(m.group(1).upper()) - ord("A")
            if 0 <= idx < n_options:
                return idx
        return None

    @staticmethod
    def _extract_evidence_from_row(row: pd.Series) -> "tuple[str, str]":
        """Extract evidence string + the source field name from a golden row."""
        # Try natural_language first (readable description of the rule).
        for field_name in ("natural_language", "original_instance", "entity_labels"):
            val = row.get(field_name, "")
            if not _is_missing(val) and str(val).strip():
                return str(val).strip(), field_name
        return "", ""

    @staticmethod
    def _rule_based_paraphrase(question: str) -> str:
        """
        Simple rule-based paraphrase for questions following the
        "Given that X, ..." pattern. NOT the paper's method — only used when
        ``allow_rule_based_fallback=True``.
        """
        q = question.strip()

        m = re.match(r"^Given that (.+?),\s*(is it true that .+)$", q, re.IGNORECASE)
        if m:
            premise, query = m.groups()
            return f"{query.rstrip('?')}, given that {premise}?"

        m = re.match(r"^Given that (.+?),\s*(what .+)$", q, re.IGNORECASE)
        if m:
            premise, query = m.groups()
            return f"Based on the fact that {premise}, {query}"

        m = re.match(r"^Based on the premise that (.+?),\s*(.+)$", q, re.IGNORECASE)
        if m:
            premise, query = m.groups()
            return f"Given that {premise}, {query}"

        m = re.match(r"^Given that (.+?),\s*(which .+)$", q, re.IGNORECASE)
        if m:
            premise, query = m.groups()
            return f"Considering that {premise}, {query}"

        return f"Please answer the following: {q}"


