def _safe_get(row: Optional[pd.Series], key: str, default: str = "") -> str:
    if row is None:
        return default
    val = row.get(key, default)
    if _is_missing(val):
        return default
    return str(val)


def _is_missing(value: Any) -> bool:
    """Robust missing-value check (handles None, NaN, list/array edge cases)."""
    if value is None:
        return True
    if isinstance(value, (dict, list, tuple)):
        return False
    try:
        return bool(pd.isna(value))
    except (TypeError, ValueError):
        return False


def _is_async_callable(fn: Optional[Callable]) -> bool:
    """True if ``fn`` is an async callable (coroutine function)."""
    if fn is None:
        return False
    return inspect.iscoroutinefunction(fn)
