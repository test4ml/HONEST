# Prompt used to back-translate a (possibly non-English) follow-up answer into
# English before QMR2 voting. [AUTHORED] — the paper says answers are
# "back-translated into English" but does not release the exact prompt.
BACK_TRANSLATE_PROMPT = (
    "Translate the following text into English. If it is already in English, "
    "return it unchanged. Output ONLY the translation, with no explanation.\n\n"
    "Text: {text}"
)


@dataclass
class DrHallEvalResult:
    """Result of evaluating a single follow-up answer."""
    file_path: str
    row_index: int
    mr_type: str
    mr_display: str
    original_question: str
    follow_up_question: str
    original_answer: str
    follow_up_answer: str
    is_violation: bool
    is_violation_nli: bool
    label: str
    confidence: float
    question_type: str
    kg_rule: str
    honest_mutation_type: str
    # QMR2-voting bookkeeping (empty/False for non-QMR2 rows and when voting
    # is disabled). For a voted QMR2 row, ``follow_up_answer`` holds the
    # *voted* answer and these fields record how it was derived.
    qmr2_voted: bool = False
    qmr2_vote_counts: str = ""        # JSON of {answer: count}
    qmr2_tie: bool = False
    qmr2_back_translated: bool = False


def detect_question_type(row: pd.Series) -> str:
    """Detect question type from the row."""
    qt = row.get("original_question_type", "")
    if pd.notna(qt) and qt:
        return str(qt)
    return "unknown"


def is_bool_question(question_type: str) -> bool:
    """Check if the question is a boolean (yes/no or true/false) question."""
    return question_type in ("yes_no", "true_false")


def evaluate_single_mr(
    original_answer: str,
    follow_up_answer: str,
    question_type: str,
    mr_type: str,
    consistent_override: Optional[bool] = None,
) -> Tuple[bool, bool, str, float]:
    """
    Evaluate consistency between original and follow-up answer.

    Args:
        consistent_override: when not ``None``, use this precomputed consistency
            verdict (e.g. from the paper's LLM consistency judge) instead of the
            NLI ``is_same_answer`` check. ``True`` = consistent, ``False`` =
            inconsistent. When ``None``, falls back to NLI.

    Returns:
        (is_violation_exact, is_violation_nli, label, confidence)
    """
    if not original_answer or not follow_up_answer:
        return False, False, "unknown", 0.0

    if follow_up_answer.startswith("ERROR:"):
        return False, False, "error", 0.0

    bool_q = is_bool_question(question_type)

    # For AMR1 (negation question), the follow-up answer should affirm
    # the original answer (i.e., "No, the statement is not NOT true" = consistent).
    if consistent_override is not None:
        is_consistent = consistent_override
    else:
        is_consistent = is_same_answer(follow_up_answer, original_answer, is_bool=bool_q)

    if is_consistent:
        return False, False, "same", 1.0
    else:
        return True, True, "different", 0.0


def evaluate_qmr2_voting(
    original_answer: str,
    per_language_answers: List[str],
    question_type: str,
    back_translated: bool = False,
    consistent_override: Optional[bool] = None,
) -> Optional[Dict[str, Any]]:
    """
    Evaluate QMR2 the paper-faithful way (DrHall Section 3.2):

        1. Majority-vote the per-language answers.
        2. Compare the voted answer to the original answer for consistency.

    Back-translation (paper: answers are back-translated into English before
    voting) is performed **separately** by :func:`back_translate_qmr2_inplace`,
    which annotates each answer object with ``follow_up_answer_en``. The caller
    is responsible for passing the English answers here and setting
    ``back_translated`` accordingly.

    Args:
        original_answer: the LLM answer to the original (English) question.
        per_language_answers: the (already back-translated) follow-up answers.
        question_type: detected question type (for boolean handling).
        back_translated: whether ``per_language_answers`` were back-translated.

    Returns:
        dict with keys: voted_answer, vote_counts, tie, back_translated,
        is_violation, is_violation_nli, label, confidence — or ``None`` if the
        voted answer could not be derived.
    """
    if not original_answer:
        return None

    # Filter out empty / error answers before voting.
    usable = [
        str(a).strip() for a in per_language_answers
        if a and str(a).strip() and not str(a).strip().startswith("ERROR:")
    ]
    if not usable:
        return None

    vote = DrHall.qmr2_vote(usable)
    voted_answer = vote["voted_answer"]
    if voted_answer is None:
        return None

    is_violation, is_violation_nli, label, confidence = evaluate_single_mr(
        original_answer, voted_answer, question_type, "QMR2",
        consistent_override=consistent_override,
    )

    return {
        "voted_answer": voted_answer,
        "vote_counts": vote["vote_counts"],
        "tie": vote["tie"],
        "back_translated": back_translated,
        "is_violation": is_violation,
        "is_violation_nli": is_violation_nli,
        "label": label,
        "confidence": confidence,
    }


async def back_translate_qmr2_inplace(
    df: pd.DataFrame,
    llm_client,
    model_name: str,
    max_concurrent: int = 20,
) -> int:
    """
    Async pre-pass: back-translate every *voted* MR column's per-language
    follow-up answers (QMR2 and CMR3) to English via the LLM, storing the result
    as ``follow_up_answer_en`` inside each answer object (paper Section 3.2:
    answers are back-translated before voting). Mutates ``df`` in place. Skips
    answers already translated (resumable) and empty/error answers.

    Runs concurrently (max_concurrent translations at a time), much faster than
    sequential awaiting.

    The function name is retained for backwards compatibility (the QMR2 voting
    path was the original user); it now also covers CMR3, which shares the same
    path was the original user); it now also covers CMR3, which shares the same
    3-language voting tail.

    Returns the number of newly translated answers.
    """
    sem = asyncio.Semaphore(max_concurrent)
    pending = []   # (a_dict, ans_text)
    cells = {}     # (idx, answer_col) -> answers_list (to write back)

    for voted_col in VOTED_COLUMNS:
        answer_col = f"{voted_col}_{model_name}_answers"
        if answer_col not in df.columns:
            continue
        for idx in df.index:
            cell = df.at[idx, answer_col]
            if pd.isna(cell) or not cell:
                continue
            try:
                answers = json.loads(str(cell))
            except (json.JSONDecodeError, TypeError):
                continue
            if not isinstance(answers, list):
                continue
            touched = False
            for a in answers:
                if not isinstance(a, dict):
                    continue
                if a.get("follow_up_answer_en"):  # already translated (resume)
                    continue
                ans = str(a.get("follow_up_answer", "")).strip()
                if not ans or ans.startswith("ERROR:"):
                    continue
                pending.append((a, ans))
                touched = True
            if touched:
                cells[(idx, answer_col)] = answers

    async def job(a, ans):
        async with sem:
            try:
                en = await llm_client.generate_answer(BACK_TRANSLATE_PROMPT.format(text=ans))
                en = str(en).strip() if en else ""
                if en:
                    a["follow_up_answer_en"] = en
            except Exception as exc:  # noqa: BLE001
                logger.warning("back-translation failed: %s", exc)

    await asyncio.gather(*[job(a, ans) for (a, ans) in pending])

    for (idx, answer_col), answers in cells.items():
        df.at[idx, answer_col] = json.dumps(answers, ensure_ascii=False)

    return len(pending)


async def _llm_judge_pair(
    llm_client,
    original_answer: str,
    follow_up_answer: str,
    simplify: bool,
) -> Optional[bool]:
    """
    Paper Section 3.5 consistency oracle via an LLM.

    Optionally simplifies both answers to one sentence (``ANSWER_SIMPLIFY_PROMPT``,
    paper verbatim), then asks the paper's ``CONSISTENCY_JUDGE_PROMPT`` and
    parses the verdict (``parse_consistency``). Returns ``True`` (consistent),
    ``False`` (inconsistent) or ``None`` (unparseable).
    """
    a = original_answer
    b = follow_up_answer
    if simplify:
        try:
            sa = await llm_client.generate_answer(DrHall.build_simplify_prompt(a))
            sa = str(sa).strip() if sa else ""
            if sa:
                a = sa
            sb = await llm_client.generate_answer(DrHall.build_simplify_prompt(b))
            sb = str(sb).strip() if sb else ""
            if sb:
                b = sb
        except Exception as exc:  # noqa: BLE001
            logger.warning("LLM simplify failed, using raw answers: %s", exc)
    try:
        prompt = DrHall.build_consistency_judge_prompt(a, b)
        verdict = await llm_client.generate_answer(prompt)
        return DrHall.parse_consistency(verdict)
    except Exception as exc:  # noqa: BLE001
        logger.warning("LLM consistency judge failed: %s", exc)
        return None


async def judge_consistency_inplace(
    df: pd.DataFrame,
    llm_client,
    model_name: str,
    active_mr_columns: List[str],
    simplify: bool = True,
    max_concurrent: int = 20,
) -> int:
    """
    Async pre-pass: for every follow-up answer (across all MR columns) compute
    the paper's LLM consistency verdict against the row's original answer and
    store it as ``consistency_llm`` (True/False/None) inside each answer object.

    For QMR2, the back-translated English answer (``follow_up_answer_en``, if
    populated) is judged in preference to the raw per-language answer. Mutates
    ``df`` in place and skips already-judged answers (resumable).

    Optimization + concurrency:
    - Only pairs that is_same_answer flags as "different" (violation) are sent to
      the strong LLM judge ("same" is high-precision, trust it directly). This
      cuts LLM calls down to violations only and corrects cross-lingual / negation
      false positives.
    - These LLM judgments run concurrently (max_concurrent), much faster than
      sequential awaiting.

    Returns the number of newly LLM-judged answers.
    """
    original_col = f"original_question_{model_name}_answer"
    sem = asyncio.Semaphore(max_concurrent)
    pending = []   # (a_dict, original, cand)
    cells = {}     # (idx, answer_col) -> answers_list (to write back)

    for idx in df.index:
        if original_col not in df.columns:
            break
        original = df.at[idx, original_col]
        if pd.isna(original) or not str(original).strip():
            golden = df.at[idx, "original_correct_answer"] if "original_correct_answer" in df.columns else ""
            original = str(golden).strip() if pd.notna(golden) else ""
        if not original:
            continue
        original = str(original).strip()
        bool_q = is_bool_question(detect_question_type(df.loc[idx]))

        for mr_col in active_mr_columns:
            answer_col = f"{mr_col}_{model_name}_answers"
            if answer_col not in df.columns:
                continue
            cell = df.at[idx, answer_col]
            if pd.isna(cell) or not cell:
                continue
            try:
                answers = json.loads(str(cell))
            except (json.JSONDecodeError, TypeError):
                continue
            if not isinstance(answers, list):
                continue

            touched = False
            for a in answers:
                if not isinstance(a, dict):
                    continue
                if a.get("consistency_llm") is not None:  # resume
                    continue
                cand = a.get("follow_up_answer_en")
                if cand is None or not str(cand).strip():
                    cand = a.get("follow_up_answer", "")
                cand = str(cand).strip()
                if not cand or cand.startswith("ERROR:"):
                    continue
                # Optimization: trust is_same_answer "same" verdicts directly
                # (high precision); only "different" pairs go to the strong LLM judge.
                if is_same_answer(cand, original, is_bool=bool_q):
                    a["consistency_llm"] = True
                    touched = True
                    continue
                pending.append((a, original, cand))
                touched = True
            if touched:
                cells[(idx, answer_col)] = answers

    async def job(a, original, cand):
        async with sem:
            a["consistency_llm"] = await _llm_judge_pair(llm_client, original, cand, simplify)

    await asyncio.gather(*[job(a, o, c) for (a, o, c) in pending])

    for (idx, answer_col), answers in cells.items():
        df.at[idx, answer_col] = json.dumps(answers, ensure_ascii=False)
    return len(pending)


def process_file(
    df: pd.DataFrame,
    model_name: str,
    file_path: str,
    active_mr_columns: List[str],
    qmr2_vote_enabled: bool = True,
    consistency_judge: str = "nli",
) -> Tuple[List[DrHallEvalResult], Dict]:
    """Process a single CSV file and evaluate all DrHall MRs.

    When ``qmr2_vote_enabled`` is True, the QMR2 column is evaluated via the
    paper's majority-voting oracle (``evaluate_qmr2_voting``) producing **one**
    result per row, instead of three independent per-language evaluations.
    Back-translation is handled separately by :func:`back_translate_qmr2_inplace`
    before this function is called.
    """
    results: List[DrHallEvalResult] = []
    stats = {
        "total": 0,
        "violations": 0,
        "by_mr": defaultdict(lambda: {"total": 0, "violations": 0}),
    }

    original_col = f"original_question_{model_name}_answer"

    for idx, row in df.iterrows():
        # Get original LLM answer
        original_answer = ""
        if original_col in df.columns and pd.notna(row.get(original_col)):
            original_answer = str(row[original_col]).strip()
        if not original_answer or original_answer.startswith("ERROR:"):
            # Fallback to golden truth
            golden = row.get("original_correct_answer", "")
            if pd.notna(golden):
                original_answer = str(golden).strip()

        if not original_answer:
            continue

        question_type = detect_question_type(row)
        kg_rule = str(row.get("kg_rule", ""))
        honest_mutation_type = str(row.get("mutation_type", ""))
        original_question = str(row.get("original_question", ""))

        # Evaluate each MR column
        for mr_col in active_mr_columns:
            answer_col = f"{mr_col}_{model_name}_answers"

            if answer_col not in df.columns:
                continue

            answers_str = row.get(answer_col, "")
            if pd.isna(answers_str) or not answers_str:
                continue

            try:
                answers = json.loads(str(answers_str))
            except (json.JSONDecodeError, TypeError):
                continue

            if not isinstance(answers, list):
                continue

            # --- Voted MRs (QMR2 and CMR3): paper-faithful majority voting ---
            # Both store three per-language follow-up answers that are voted into
            # one result per row (paper §3.2 for QMR2; §3.4/CMR3 reuses the same
            # 3-language voting tail).
            if mr_col in VOTED_COLUMNS and qmr2_vote_enabled:
                # Prefer back-translated English answers (populated by
                # back_translate_qmr2_inplace); fall back to raw answers.
                per_language_answers = []
                consistency_by_text: Dict[str, Any] = {}
                bt_count = 0
                for a in answers:
                    if not isinstance(a, dict):
                        continue
                    en = a.get("follow_up_answer_en")
                    if en is not None and str(en).strip():
                        cand = str(en).strip()
                        bt_count += 1
                    else:
                        cand = str(a.get("follow_up_answer", "")).strip()
                    if not cand or cand.startswith("ERROR:"):
                        continue
                    per_language_answers.append(cand)
                    if consistency_judge == "llm":
                        consistency_by_text[cand] = a.get("consistency_llm")
                back_translated = bt_count > 0
                # For the LLM judge, look up the precomputed consistency of the
                # (eventual) majority-voted answer; the voted answer is always
                # one of the candidate strings, so it is present in the map.
                voted_override: Optional[bool] = None
                if consistency_judge == "llm" and per_language_answers:
                    pre_vote = DrHall.qmr2_vote(per_language_answers)
                    if pre_vote["voted_answer"] is not None:
                        voted_override = consistency_by_text.get(
                            pre_vote["voted_answer"]
                        )
                voted = evaluate_qmr2_voting(
                    original_answer,
                    per_language_answers,
                    question_type,
                    back_translated=back_translated,
                    consistent_override=voted_override,
                )
                if voted is None:
                    continue

                mr_display = MR_DISPLAY_NAMES.get(mr_col, mr_col)
                stats["total"] += 1
                stats["by_mr"][mr_display]["total"] += 1
                if voted["is_violation"]:
                    stats["violations"] += 1
                    stats["by_mr"][mr_display]["violations"] += 1

                # Concatenate the per-language follow-up questions for auditability.
                fu_questions = " ||| ".join(
                    str(a.get("follow_up_question", "")).strip()
                    for a in answers if isinstance(a, dict)
                )
                results.append(DrHallEvalResult(
                    file_path=file_path,
                    row_index=int(idx) if idx is not None else -1,
                    mr_type=mr_display,
                    mr_display=mr_display,
                    original_question=original_question,
                    follow_up_question=fu_questions,
                    original_answer=original_answer,
                    follow_up_answer=voted["voted_answer"],
                    is_violation=voted["is_violation"],
                    is_violation_nli=voted["is_violation_nli"],
                    label=voted["label"],
                    confidence=voted["confidence"],
                    question_type=question_type,
                    kg_rule=kg_rule,
                    honest_mutation_type=honest_mutation_type,
                    qmr2_voted=True,
                    qmr2_vote_counts=json.dumps(voted["vote_counts"], ensure_ascii=False),
                    qmr2_tie=voted["tie"],
                    qmr2_back_translated=voted["back_translated"],
                ))
                continue

            # --- All other MRs: evaluate each follow-up answer independently ---
            for ans_obj in answers:
                if not isinstance(ans_obj, dict):
                    continue

                follow_up_answer = str(ans_obj.get("follow_up_answer", "")).strip()
                follow_up_question = str(ans_obj.get("follow_up_question", "")).strip()
                mr_type = str(ans_obj.get("mr_type", mr_col))

                if not follow_up_answer or follow_up_answer.startswith("ERROR:"):
                    continue

                stats["total"] += 1
                mr_display = MR_DISPLAY_NAMES.get(mr_col, mr_col)
                stats["by_mr"][mr_display]["total"] += 1

                try:
                    override = None
                    if consistency_judge == "llm":
                        override = ans_obj.get("consistency_llm")
                    is_violation, is_violation_nli, label, confidence = evaluate_single_mr(
                        original_answer, follow_up_answer, question_type, mr_type,
                        consistent_override=override,
                    )
                except Exception as exc:
                    logger.warning(f"Error evaluating {mr_col} for row {idx}: {exc}")
                    continue

                if is_violation:
                    stats["violations"] += 1
                    stats["by_mr"][mr_display]["violations"] += 1

                results.append(DrHallEvalResult(
                    file_path=file_path,
                    row_index=int(idx) if idx is not None else -1,
                    mr_type=mr_type,
                    mr_display=mr_display,
                    original_question=original_question,
                    follow_up_question=follow_up_question,
                    original_answer=original_answer,
                    follow_up_answer=follow_up_answer,
                    is_violation=is_violation,
                    is_violation_nli=is_violation_nli,
                    label=label,
                    confidence=confidence,
                    question_type=question_type,
                    kg_rule=kg_rule,
                    honest_mutation_type=honest_mutation_type,
                ))

    return results, dict(stats)


def calculate_statistics(results: List[DrHallEvalResult]) -> Dict:
    """Calculate overall and per-MR statistics."""
    total = len(results)
    violations = sum(1 for r in results if r.is_violation)
    violation_rate = violations / total if total > 0 else 0.0

    by_mr: Dict[str, Dict] = {}
    by_mr_display = defaultdict(lambda: {"total": 0, "violations": 0})
    for r in results:
        by_mr_display[r.mr_display]["total"] += 1
        if r.is_violation:
            by_mr_display[r.mr_display]["violations"] += 1

    for mr_name, mr_stats in by_mr_display.items():
        t = mr_stats["total"]
        v = mr_stats["violations"]
        by_mr[mr_name] = {
            "total": t,
            "violations": v,
            "violation_rate": v / t if t > 0 else 0.0,
        }

    # Also group by question type
    by_question_type: Dict[str, Dict] = {}
    qt_groups = defaultdict(lambda: {"total": 0, "violations": 0})
    for r in results:
        qt_groups[r.question_type]["total"] += 1
        if r.is_violation:
            qt_groups[r.question_type]["violations"] += 1

    for qt, qt_stats in qt_groups.items():
        t = qt_stats["total"]
        v = qt_stats["violations"]
        by_question_type[qt] = {
            "total": t,
            "violations": v,
            "violation_rate": v / t if t > 0 else 0.0,
        }

    # Group by honest_mutation_type
    by_honest_mutation: Dict[str, Dict] = {}
    kmt_groups = defaultdict(lambda: {"total": 0, "violations": 0})
    for r in results:
        kmt_groups[r.honest_mutation_type]["total"] += 1
        if r.is_violation:
            kmt_groups[r.honest_mutation_type]["violations"] += 1

    for kmt, kmt_stats in kmt_groups.items():
        t = kmt_stats["total"]
        v = kmt_stats["violations"]
        by_honest_mutation[kmt] = {
            "total": t,
            "violations": v,
            "violation_rate": v / t if t > 0 else 0.0,
        }

    return {
        "total": total,
        "violations": violations,
        "violation_rate": violation_rate,
        "by_mr": by_mr,
        "by_question_type": by_question_type,
        "by_honest_mutation_type": by_honest_mutation,
    }


def auto_detect_model_name(input_path: Path) -> str:
    """Extract model name from directory name."""
    prefix = "golden_dataset_drhall_answer_"
    if input_path.name.startswith(prefix):
        return input_path.name[len(prefix):]
    return "Qwen2.5_7B_Instruct"


def convert_for_json(obj):
    """Convert numpy types for JSON serialization."""
    if isinstance(obj, np.integer):
        return int(obj)
    elif isinstance(obj, np.floating):
        return float(obj)
    elif isinstance(obj, dict):
        return {k: convert_for_json(v) for k, v in obj.items()}
    elif isinstance(obj, list):
        return [convert_for_json(item) for item in obj]
    return obj


