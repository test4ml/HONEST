"""
RQ4 DrHall Evaluation Script

Evaluates DrHall metamorphic relations by checking answer consistency between
the original question answer and follow-up question answers for each MR.

DrHall Evaluation Logic:
1. Get the LLM answer to the ORIGINAL question
2. Get the LLM answer to each FOLLOW-UP question (generated by MRs)
3. Check consistency: if answers are inconsistent -> hallucination detected
4. Calculate violation rate per MR and overall

Consistency checking uses the same NLI-based approach as other baselines
(QAQA, QAAskeR) via is_same_answer().

Usage:
    conda activate karma
    # Evaluate DrHall mutations with LLM answers
    python scripts/rqs/rq4_drhall_evaluation.py \
        --input-dir data/examples/golden_dataset_drhall_answer_Qwen2.5_7B_Instruct

    # Specify model name explicitly
    python scripts/rqs/rq4_drhall_evaluation.py \
        --input-dir data/examples/golden_dataset_drhall_answer_Qwen2.5_7B_Instruct \
        --model-name Qwen2.5_7B_Instruct
"""

